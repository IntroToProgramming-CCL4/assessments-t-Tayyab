# Import the sys module, which provides access to some variables used or maintained by the Python interpreter.
import sys

# Print a message indicating that the following output is the Python version.
print("python version :")

# Use the sys.version attribute to obtain and print the Python interpreter version.
print(sys.version)

# Print a message indicating that the following output is version information.
print("version info :")

# Use the sys.version_info attribute to obtain and print the Python interpreter version.
print(sys.version_info)
