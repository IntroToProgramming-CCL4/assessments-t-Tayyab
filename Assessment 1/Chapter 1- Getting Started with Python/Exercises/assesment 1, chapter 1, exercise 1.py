# The following code uses the print function in Python to display the lyrics of the nursery rhyme "Twinkle, Twinkle, Little Star."
# Each line is separated by a newline character (\n) to create the structure of the poem.

print('Twinkle, twinkle, little star,')  # Print the first line
print('\t How I wonder what you are!')   # Print the second line with a tab (\t) for indentation
print('\t\t Up above the world so high,')  # Print the third line with double tabs for indentation
print('\t\t Like a diamond in the sky.')  # Print the fourth line with double tabs for indentation
print('Twinkle, twinkle, little star,')  # Print the fifth line, repeating the first line
print('\t How I wonder what you are')  # Print the sixth line without an exclamation mark for consistency
