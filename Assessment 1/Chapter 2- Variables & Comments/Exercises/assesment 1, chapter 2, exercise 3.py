# Create a variable with whitespace characters
name = "\t \n Tayyab \t\n"

# Print the name 
print("Name with some whitespace:")
print(name)

# Print the name with lstrip functions
print("\nName using lstrip:")
print(name.lstrip())

# Print the name with rstrip functions
print("\nName using rstrip:")
print(name.rstrip())

# Print the name with strip functions
print("\nName using strip:")
print(name.strip())